type t = { index: int; }

type type_var = t Algebra.Core.type_var
type type_term = [
    type_var
  | `Cons of Algebra.TypeCons.type_cons * type_term list
]

let lift x = { index = x.index + 1 }

let zero = { index = 0 }

let string_of_t x = string_of_int x.index
